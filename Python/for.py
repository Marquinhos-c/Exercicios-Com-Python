#calculando o bonus de aumento do funcionario que atinge a meta.

meta = 1200
bonus = 0.1
f = int(input('Quantos funcionarios? '))

for i in range(1,f+1):
    venda = float(input(f'Digite o valor mensal de vendas do vendedor {i}: '))
    if venda >= meta:
        aumento = bonus * venda
    else:
        aumento = 0
    
    print(aumento)